package gg.squire.mixology;

/**
 * Enum representing the operating mode of the Mixology plugin.
 */
public enum PluginMode {
    /**
     * Run the full Mixology minigame from start to finish.
     */
    MINIGAME,

    /**
     * Only create herb paste without running the full minigame.
     */
    CREATE_PASTE
}
