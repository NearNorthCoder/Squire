package gg.squire.leagues.common;

/**
 * Enumeration of different rock types that can be mined in OSRS.
 * Used by the Leagues Miner plugin to determine which rocks to mine.
 */
public enum RockType {
    /** Runite ore rocks */
    RUNE,

    /** Adamantite ore rocks */
    ADDY,

    /** Mithril ore rocks */
    MITHRIL,

    /** Iron ore rocks */
    IRON,

    /** Coal rocks */
    COAL,

    /** Gold ore rocks */
    GOLD,

    /** Soft clay rocks */
    SOFTCLAY,

    /** Silver ore rocks */
    SILVER
}
