package gg.squire.combat;

/**
 * Enum representing the different combat styles available for attacking blue dragons.
 * Players can choose between melee, magic, or ranged combat.
 */
public enum AttackStyle {
    /**
     * Melee combat style - close-range physical attacks
     */
    MELEE,

    /**
     * Magic combat style - ranged magical attacks
     */
    MAGIC,

    /**
     * Ranged combat style - bow/crossbow attacks
     */
    RANGE
}
